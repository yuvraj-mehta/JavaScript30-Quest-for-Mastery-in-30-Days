//Task 1: Write a program to print numbers from 1 to 10 using a for loop.

console.log("Task 1: Printing numbers from 1-10.");
for(let i = 1; i <= 10; i++) {
  console.log(i);
}

//Task 2: Write a program to print the multiplication table of 5 using a for loop.
console.log("Task 2: Printing multiplication of 5.");
for(let i = 1; i <= 10; i++) {
  console.log(i * 5);
}